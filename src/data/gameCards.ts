export const cultureCards = gameCards.filter((card) => card.category === 'culture')
export const eventCards = gameCards.filter((card) => card.category === 'event')
export const achievementCards = gameCards.filter((card) => card.category === 'achievement')
export const clueCards = gameCards.filter((card) => card.category === 'clue')
export const utilityCards = gameCards.filter((card) => card.category === 'utility')
export const roleCards = gameCards.filter((card) => card.category === 'role')

export const playableCardCategories: GameCardCategory[] = ['culture', 'event', 'achievement', 'clue', 'utility', 'role']

export const defaultSelectedCardId = gameCards[0].id

export const gameCardCount = gameCards.length

export function getGameCard(cardId: string) {
  return gameCards.find((card) => card.id === cardId)
}

export function getGameCards(cardIds: string[]) {
  return cardIds.map((cardId) => getGameCard(cardId)).filter((card): card is GameCard => Boolean(card))
}

export function getGameCardsByCategory(category: GameCardCategory) {
  return gameCards.filter((card) => card.category === category)
}

export function getCardsForHand(collectedCardIds: string[]) {
  const collected = getGameCards(collectedCardIds)
  const pinned = cultureCards
  const additions = collected.filter((card) => card.category !== 'culture')
  return Array.from(new Map([...pinned, ...additions].map((card) => [card.id, card])).values())
}

export function getGameCardImageUrl(card: GameCard) {
  return `/assets/beijing/${card.image}`
}
