type ShanghaiCardInput = {
  id: string
  name: string
  category: GameCardCategory
  deck: GameCardDeck
  theme: string
  description: string
  color: string
  icon: LucideIcon
  tags: string[]
  unlockRule: string
  effect?: string
  nodeAffinity?: string[]
  role?: GameCard['role']
}

function card(no: number, input: ShanghaiCardInput): GameCard {
  return {
    no,
    image: `deck/card-${String(no).padStart(2, '0')}-${input.id}.png`,
    ...input,
  }
}

